    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String Moeda = String.valueOf((String) jComboBox1.getSelectedItem());
        // Float Valor = Float.valueOf((String)jTextField2.getSelectedText()+","+jTextField3.getSelectedText());
        serviços serv = new serviços();
        serv.setNome(jTextField1.getText());
        serv.setValor(jTextField2.getText() + "." + jTextField3.getText());
        serv.setMoeda(Moeda);
        serv.setCom(jTextArea1.getText());
        if ((jTextField1.getText().isEmpty()) || (jTextField2.getText().length() < 0) || (jTextField2.getText().length() > 8) || (jTextField3.getText().length() != 2)) {
            JOptionPane.showMessageDialog(null, "Confira os dados");
        } else {
            ServiçosDAO dao = null;
            try {
                dao = new ServiçosDAO();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JDialogCadst.class.getName()).log(Level.SEVERE, null, ex + ex.getMessage());
            }
            dao.adiciona(serv);
            JOptionPane.showMessageDialog(null, " " + jTextField1.getText() + "\nAlterado com sucesso! ");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextArea1.setText("");
        }int showConfirmDialog = JOptionPane.showConfirmDialog(null, "Outro Cadastro?");
        if((showConfirmDialog == 1)){
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

