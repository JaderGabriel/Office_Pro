    private void jTextField15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField15ActionPerformed
        if ((jTextField15.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        } else {

            try {
                Proprietario p = UsuarioDAO.procuraIDUser(jTextField15.getText());
                prop = p;
                if (p == null) {
                    JOptionPane.showMessageDialog(this, "N√ÉO ENCONTRADO");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField7.setText("");
                    jTextField8.setText("");
                    jTextField9.setText("");
                    jTextField10.setText("");
                    jTextField11.setText("");
                    jTextField14.setText("");
                    jLabel22.setText("");
                    jLabel21.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "ENCONTRADO");
                    jTextField1.setText(prop.getRg());
                    jTextField2.setText(prop.getNome());
                    jTextField3.setText(prop.getCpfcnpj());
                    jTextField4.setText(prop.getEmail());
                    jTextField5.setText(prop.getTelefoneF());
                    jTextField6.setText(prop.getEnd());
                    jTextField7.setText(prop.getNro());
                    jTextField8.setText(prop.getComplemento());
                    jTextField9.setText(prop.getBairro());
                    jTextField10.setText(prop.getCidade());
                    jLabel22.setText(prop.getLogradouro());
                    jLabel21.setText(prop.getUf());
                    jTextField11.setText(prop.getCep());
                    jTextField14.setText(prop.getTefeloneC());


                }
            } catch (SQLException ex) {
                Logger.getLogger(JDialogRealiza.class.getName()).log(Level.SEVERE, null, ex);
            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTextField15ActionPerformed

