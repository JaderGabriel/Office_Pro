    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean OK = false;
        try {
            ValidarDAO dao = new ValidarDAO(); 
           if ((jTextField1.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos nÃ£o podem retornar vazios");
        }
        if (jTextField1.getText().length()>=15) {
            JOptionPane.showMessageDialog(null, "Campo Nome tem que ser menor que 15 caracteres");
        } else {
                
              boolean procurar = ValidarDAO.buscarUser(jTextField1.getText());
              if(procurar == false){
                  OK=true;
              }
              if(procurar == true){
              JOptionPane.showMessageDialog(null, "USUARIO JA EXISTE");
              }
            }
          if (OK == true){
              Validar uk = new Validar();
              String Tipo = String.valueOf((String) jComboBox1.getSelectedItem());
              uk.setUsuario(jTextField1.getText());
              String T = jPasswordField1.getText();
              Criptografia crip = new Criptografia();
              crip.setSenha(T);
              uk.setSenha(crip.getSenha());
              uk.setTipo(Tipo);
                boolean adiciona = dao.adiciona(uk);
                if(adiciona == true){
              JOptionPane.showMessageDialog(null, jTextField1.getText() + "\n " + Tipo + "\nInserido com sucesso! ");
              jTextField1.setText("");
              jPasswordField1.setText("");
          }
        } 
         
        int showConfirmDialog = JOptionPane.showConfirmDialog(null, "Outro Cadastro?");
        if((showConfirmDialog == 0)){
           jTextField1.setText("");
              jPasswordField1.setText("");
        }
        if((showConfirmDialog == 1)){
            this.dispose();
        }
          }
        catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

